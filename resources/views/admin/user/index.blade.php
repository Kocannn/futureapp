document.getElementById('filter-role').addEventListener('change', function(e) {
    const selectedRole = e.target.value;
    const rows = document.querySelectorAll('[data-role]');

    rows.forEach(row => {
        if (selectedRole === '' || row.getAttribute('data-role') === selectedRole) {
            row.style.display = '';
        } else {
            row.style.display = 'none';
        }
    });
});

// Filter by period
document.getElementById('filter-period').addEventListener('change', function(e) {
    const selectedPeriod = e.target.value;
    const rows = document.querySelectorAll('[data-date]');
    const today = new Date().toISOString().split('T')[0];
    const weekAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000).toISOString().split('T')[0];
    const monthAgo = new Date(Date.now() - 30 * 24 * 60 * 60 * 1000).toISOString().split('T')[0];

    rows.forEach(row => {
        const rowDate = row.getAttribute('data-date');
        let show = true;

        if (selectedPeriod === 'today') {
            show = rowDate === today;
        } else if (selectedPeriod === 'week') {
            show = rowDate >= weekAgo;
        } else if (selectedPeriod === 'month') {
            show = rowDate >= monthAgo;
        }

        row.style.display = show ? '' : 'none';
    });
});

