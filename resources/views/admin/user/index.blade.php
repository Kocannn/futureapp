<script>
function confirmDelete(id, name) {
    const modal = document.getElementById('delete-modal');
    const form = document.getElementById('delete-form');
    const itemName = document.getElementById('delete-item-name');

    form.action = `/admin/user/${id}`;
    itemName.textContent = name;
    modal.classList.remove('hidden');
    modal.classList.add('flex');
}

document.getElementById('cancel-delete').addEventListener('click', () => {
    const modal = document.getElementById('delete-modal');
    modal.classList.add('hidden');
    modal.classList.remove('flex');
});

// Search functionality
document.getElementById('search-user').addEventListener('input', function(e) {
    const searchTerm = e.target.value.toLowerCase();
    const rows = document.querySelectorAll('tbody tr, .lg\\:hidden > div');

    rows.forEach(row => {
        const text = row.textContent.toLowerCase();
        if (text.includes(searchTerm)) {
            row.style.display = '';
        } else {
            row.style.display = 'none';
        }
    });
});

